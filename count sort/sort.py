# sort adalah mengurutkan data

menu =["ayam","ikan","gajah","jerapah"]
# kita inisialisi variabel

menu.sort()
# kita mengurutkan dari alfabet a

print(menu) # outputnya adalah ayam, gajah, ikan, jerapah

