# sort adalah mengurutkan data

menu =["ayam","ikan","gajah","jerapah"]
# kita inisialisi variabel

menu.sort()
# kita mengurutkan dari alfabet a

print(menu) # outputnya adalah ayam, gajah, ikan, jerapah

# kita coba buat mengurutkan dari alfabet z
data = ["ayam","ikan","gajah","jerapah"]

data.sort(reverse=True)
# kita menambahkan reverse untuk mengubah urutan dari belakang
print(data) # outputnya adalah jerapah, ikan, gajah, ayam
