# list data type
# list data type adalah sebuah array
# array adalah suatu tipe data terstruktur yang dapat menyimpan data yang sangat banyak
# pada suatu nama yang sama
# array dimulai dari 0,1,2,3


makanan = ["bakso","sate","seblak","soto","mi goreng"]
#  nama array = [0,1,2,3,4]
print(makanan[0]) # akan menampilkan bakso
print(makanan[1]) # akan menampilkan sate
print(makanan[2]) # akan menampilkan seblak
print(makanan[3]) # akan menampilkan soto
print(makanan[4]) # akan menampilkan mi goreng

# array juga bisa dipanggil sebagian,
# contohnya kita mau memanggil seblak sampe mi goreng
print(makanan[2:5]) # akan keluar seblak sampai mi goreng